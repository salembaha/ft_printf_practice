#include "ft_printf.h"

int	ft_print_u(unsigned int n)
{
	int	i;

	i = 0;
	if (n >= 10)
	{
		i += ft_print_u(n / 10);
		if (i == -1)
			return (-1);
	}
	if (ft_print_c('0' + (n % 10)) == -1)
		return (-1);
	return (i + 1);
}
